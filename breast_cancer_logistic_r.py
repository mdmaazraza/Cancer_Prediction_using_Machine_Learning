import numpy as np
import pandas as pd
import sklearn.datasets
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

data = sklearn.datasets.load_breast_cancer()
print(data)

data_frame = pd.DataFrame(data.data, columns = data.feature_names)
print(data_frame)

data_frame.head()

data_frame['label'] = data.target
data_frame.tail()

data_frame.describe()

data_frame['label'].value_counts()

data_frame.groupby('label').mean()

X = data_frame.drop(columns='label',axis=1)
Y = data_frame['label']

print(X)

[X_train,X_test,Y_train,Y_test]=train_test_split(X,Y,test_size=0.2,stratify = Y,random_state=2)
print(X.shape, X_train.shape, X_test.shape)
lr=LogisticRegression()
lr.fit(X_train,Y_train)
training_data_prediction=lr.predict(X_train)
training_data_accuracy = accuracy_score(Y_train, training_data_prediction)
print("Accuracy on Training data is: ",training_data_accuracy)

X_test_data_prediction = lr.predict(X_test)
test_data_accuracy = accuracy_score(Y_test,X_test_data_prediction)
print("Accuracy on Test data is: ",test_data_accuracy)

